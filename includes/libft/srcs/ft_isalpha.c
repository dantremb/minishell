/* Retourne 1 si on trouve un caractère alphabétique sinon 0. 				  */

int	ft_isalpha(int nbr)
{
	if ((nbr >= 'a' && nbr <= 'z') || (nbr >= 'A' && nbr <= 'Z'))
		return (1);
	return (0);
}
